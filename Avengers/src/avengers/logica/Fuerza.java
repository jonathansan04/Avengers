package avengers.logica;

/**
 *
 * @author tatan
 */
public interface Fuerza {

    public static void resistencia() {
        /**
         * La capacidad que tiene el superhéroe para resistir los golpes del
         * rival.
         */
    }

    public static void golpe() {
        /**
         * La manera cómo golpea, la fuerza con que dirige sus golpes, dónde
         * golpea.
         */
    }

    public static void brazos() {
        /**
         * La fuerza que tiene en sus brazos y la fuerza con qué golpea con sus
         * brazos.
         */
    }

    public static void piernas() {
        /**
         * La fuerza que tiene en sus piernas, la fuerza con que golpea con sus
         * piernas.
         */
    }
    
}
